package figuraapp;

public class Punto {
    private float x, y;

    public Punto(){
        x = 1;
        y = 1;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

}
